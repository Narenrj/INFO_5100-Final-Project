    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = vaccineManu.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Please Select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        VaccineProductionRequest = (VaccineProductionRequest)vaccineManu.getValueAt(selectedRow,0);
        VaccineProductionRequest.setStatus("Approved");
        
        populateManufacturerListTable(); 
        
        VaccineManufacturerRequest vaccineManufacturerRequest2 = new VaccineManufacturerRequest
        (VaccineProductionRequest.getName(), VaccineProductionRequest.getEfficiency(), 
                VaccineProductionRequest.getManufacturername(), VaccineProductionRequest.getNumberofdoses(), "SASAS", new Date());
        vaccineManufacturerRequest2.setStatus("Approved");
        Network network = system.searchNetworkName(VaccineProductionRequest.getState());
        if(network ==null){
            network = new Network();
            network.setNetworkName(VaccineProductionRequest.getState());
            system.setNetworkList(network);
        }
        if(network.getEnterpriseDirectory() == null){
            EnterpriseDirectory ed = new EnterpriseDirectory();
            ed.createAndAddEnterprise("PublicHealthDepartment", 
                    Enterprise.EnterpriseType.PublicHealthDepartment);
            network.setEnterpriseDirectory(ed);
            
        }
        for(Enterprise e : network.getEnterpriseDirectory().getEnterpriseDirectoryList()){
                
                if(e.getEnterpriseType() == Enterprise.EnterpriseType.PublicHealthDepartment){
                    if(e.getWorkQueue()==null){
                        e.setWorkQueue(new WorkQueue());
                    }
                    e.getWorkQueue().getWorkRequestList().add(vaccineManufacturerRequest2);
                    System.out.println("Added ");
                    return;               

                }
            }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

