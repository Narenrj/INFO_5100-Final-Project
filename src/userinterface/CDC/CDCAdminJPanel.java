    public void populateManufacturerListTable(){
       
                
        DefaultTableModel dtm = (DefaultTableModel) vaccineManu.getModel();
        dtm.setRowCount(0);
        System.out.println(system.getNetworkList().size());
        for(Network n : system.getNetworkList()){
               
            for( Enterprise e : n.getEnterpriseDirectory().getEnterpriseDirectoryList()){
                for( WorkRequest workRequest : e.getWorkQueue().getWorkRequestList() ){
                    if(workRequest instanceof VaccineProductionRequest){
                        System.out.println("w "+ workRequest);
                        Object row[] = new Object[5];
                        row[0] = ((VaccineProductionRequest) workRequest);
                        row[1] = ((VaccineProductionRequest) workRequest).getEfficiency();
                        row[2] = ((VaccineProductionRequest) workRequest).getNumberofdoses();
                        row[3] = ((VaccineProductionRequest) workRequest).getStatus();
                        row[4] = ((VaccineProductionRequest) workRequest).getRequestDate();
                       
                        dtm.addRow(row);
                    }
                }
            }
               
        }
    }
   
